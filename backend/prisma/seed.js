async function main() {
  const adminPasswordHash = await bcrypt.hash('Admin12345', 12);
  const eventoriaPasswordHash = await bcrypt.hash('Eventoria12345', 12);

  await prisma.user.upsert({
    where: { email: 'admin@talentbridge.local' },
    update: {
      firstName: 'Admin',
      lastName: 'TalentBridge',
      role: UserRole.ADMIN,
    },
    create: {
      email: 'admin@talentbridge.local',
      passwordHash: adminPasswordHash,
      firstName: 'Admin',
      lastName: 'TalentBridge',
      role: UserRole.ADMIN,
    },
  });

  const companyBySlug = {};

  for (const company of companies) {
    companyBySlug[company.slug] = await prisma.company.upsert({
      where: { slug: company.slug },
      update: company,
      create: company,
    });
  }

  const eventoriaUser = await prisma.user.upsert({
    where: { email: 'eventoria@talentbridge.local' },
    update: {
      passwordHash: eventoriaPasswordHash,
      firstName: 'Eventoria',
      lastName: 'Tech',
      role: UserRole.COMPANY,
    },
    create: {
      email: 'eventoria@talentbridge.local',
      passwordHash: eventoriaPasswordHash,
      firstName: 'Eventoria',
      lastName: 'Tech',
      role: UserRole.COMPANY,
    },
  });

  await prisma.company.updateMany({
    where: {
      ownerId: eventoriaUser.id,
      NOT: { slug: 'eventoria-tech' },
    },
    data: { ownerId: null },
  });

  await prisma.company.update({
    where: { slug: 'eventoria-tech' },
    data: {
      ownerId: eventoriaUser.id,
      status: CompanyStatus.APPROVED,
    },
  });

  for (const job of jobs) {
    const { companySlug, ...jobData } = job;
    const company = companyBySlug[companySlug];

    await prisma.job.upsert({
      where: { slug: job.slug },
      update: {
        ...jobData,
        companyId: company.id,
      },
      create: {
        ...jobData,
        companyId: company.id,
      },
    });
  }

  console.log(`Seed completed: ${companies.length} companies, ${jobs.length} jobs, 1 admin user and 1 company user.`);
}

main()
  .catch((error) => {
    console.error(error);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
