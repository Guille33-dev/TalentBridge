function normalizeText(value) {
  return value
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase();
}

function inferJobCategory(job) {
  if (jobCategoryBySlug[job.slug]) return jobCategoryBySlug[job.slug];

  const text = normalizeText(
    [job.title, job.description, job.overview, ...(job.tags || []), ...(job.skills || [])].filter(Boolean).join(' '),
  );

  const hasAny = (keywords) => keywords.some((keyword) => text.includes(keyword));

  if (hasAny(['ciberseguridad', 'seguridad', 'soc', 'cybersecurity'])) return JobCategory.CYBERSECURITY;
  if (hasAny(['devops', 'cloud', 'ci/cd', 'linux'])) return JobCategory.CLOUD_DEVOPS;
  if (hasAny(['testing', 'qa', 'tester', 'calidad'])) return JobCategory.QA;
  if (hasAny(['operaciones', 'operations', 'soporte', 'comunidad'])) return JobCategory.OPERATIONS;
  if (hasAny(['contenido', 'content producer', 'social media'])) return JobCategory.CONTENT;
  if (hasAny(['producto', 'product management', 'product'])) return JobCategory.PRODUCT;
  if (hasAny(['diseno', 'ux', 'figma', 'branding'])) return JobCategory.DESIGN;
  if (hasAny(['marketing', 'seo', 'crm', 'growth', 'employer branding'])) return JobCategory.MARKETING;
  if (hasAny(['datos', 'data', 'bi', 'analytics', 'analitica', 'reporting'])) return JobCategory.DATA;

  return JobCategory.DEVELOPMENT;
}

